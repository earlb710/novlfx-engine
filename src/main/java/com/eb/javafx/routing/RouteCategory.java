package com.eb.javafx.routing;

/** High-level route groups for migrated JavaFX shell screens. */
public enum RouteCategory {
    /** Main menu and navigation hub routes. */
    MENU,
    /** Preferences and configuration routes. */
    SETTINGS,
    /** Save/load lifecycle routes. */
    SAVE_LOAD,
    /** Dialogue, say-window, and choice routes. */
    DIALOGUE,
    /** Heads-up display routes. */
    HUD,
    /** Notification, modal, and visual overlay routes. */
    OVERLAY,
    /** Tooltip/help panel routes. */
    TOOLTIP
}
